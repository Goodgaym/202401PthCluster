#include <stdio.h>

int main(){
    printf("%d \n", 10);
    printf("%c \n", 'A');
    printf("%f \n", 3.14);
    printf("%s \n", "안녕하세요.");
    printf("%x \n", 31);

    /*
    %d : decimal (10진수)
    %x : hexadecimal (16진수)
    %c : character (문자)
    %s : string (문자열)
    %f : flaoting point number (부동소수점)
    %o : octal (8진수)
    %ld : 32비트 10진수
    */

    return 0;
}